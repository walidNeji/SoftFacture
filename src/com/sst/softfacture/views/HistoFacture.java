    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

//        NumberFormat formatter = java.text.NumberFormat.getInstance(java.util.Locale.FRENCH);
        DecimalFormat format = new DecimalFormat("# ###.000");
        DefaultTableModel model = (DefaultTableModel) jXTable1.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        List<FichFacture> list = null;
        FichFactureJpaController controller = new FichFactureJpaController(DBHandler.instance());
        if (jRadioButton1.isSelected()) {
            list = controller.findbyDate(jDateChooser1.getDate());
        } else if (jRadioButton2.isSelected()) {
            list = controller.findbyNumFacture(Integer.parseInt(jTextField1.getText()));
        }
        for (FichFacture fichFacture : list) {
            model.addRow(new Object[]{fichFacture.getNumFact(), "" + fichFacture.getLibelleClient() + "",
                format.format(fichFacture.getMntHt()), format.format(fichFacture.getTotTva()), format.format(fichFacture.getMntTtc())});
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jXTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jXTable1KeyPressed
        // TODO add your handling code here:
        DecimalFormat format = new DecimalFormat("# ###.000");
        DefaultTableModel model = (DefaultTableModel) jXTable2.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        FichFactureJpaController controller = new FichFactureJpaController(DBHandler.instance());
        DefaultTableModel md = (DefaultTableModel) jXTable1.getModel();
        if (jXTable1.getSelectedRow() >= 0) {
            int numFact = Integer.parseInt(md.getValueAt(jXTable1.getSelectedRow(), 0).toString());
            FichFacture fichFacture = controller.findFichFacture(numFact);
            for (FichDetailFacture detail : fichFacture.getFichDetailFactureList()) {
                model.addRow(new Object[]{"" + detail.getLibelleProduit() + "", detail.getQte(),
                    format.format(detail.getPuHt()), detail.getTva(), format.format(detail.getQte() * detail.getPuHt())});
            }
        }
    }//GEN-LAST:event_jXTable1KeyPressed

