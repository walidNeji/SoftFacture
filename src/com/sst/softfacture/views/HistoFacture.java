    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        FichFactureJpaController controller = new FichFactureJpaController(DBHandler.instance());
        DefaultTableModel md = (DefaultTableModel) jXTable1.getModel();
        int rowselected = jXTable1.getSelectedRow();
        if (rowselected >= 0) {
            try {
                int numFact = Integer.parseInt(md.getValueAt(rowselected, 0).toString());
                controller.destroy(numFact);
                JOptionPane.showMessageDialog(null, "FACTURE SUPPRIMEE !");
                md.removeRow(rowselected);
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(HistoFacture.class.getName()).log(Level.SEVERE, null, ex);
            }


        }
    }//GEN-LAST:event_jButton2ActionPerformed
